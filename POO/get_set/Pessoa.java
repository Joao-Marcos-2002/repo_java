
// Classe pessoa: com getter e setters
public class Pessoa {
    private String nome;
    private int idade;

    // Getter: retorna o valor do atributo 'nome'
    public String getNome() {
        return nome;
    }

    // Setter: define o valor do atributo 'nome'
    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }
} 

